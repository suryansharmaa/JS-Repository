let firstFavNum = 8;
let secondFavNum = 2;

// Relational Comparison Operators
console.log("Greater :", firstFavNum > secondFavNum);
console.log("Smaller :", firstFavNum < secondFavNum);
console.log("Greater than or equal :", firstFavNum >= secondFavNum);
console.log("Smaller than or equal :", firstFavNum <= secondFavNum);

// Equality Comparison Operators
console.log("Strictly equal :", firstFavNum === secondFavNum);
console.log("Strictly non-equal :", firstFavNum !== secondFavNum);
console.log("Loosely equal :", firstFavNum == secondFavNum);
console.log("Loosely non-equal :", firstFavNum != secondFavNum);
